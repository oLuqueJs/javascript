/*
    In JS we can use LET to create a block-scoped local variable that is re-assignable
    we can optionally initialize them with a value or data type
    
*/

let charName = 'Ezio'
var birthYear = 1966;
let game;

console.log(`${charName} was born in ${birthYear}.`);

game = "Assasin's Creed II"
console.log(`${charName} appeared on ${game}.`)